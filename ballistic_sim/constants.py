"""物理常数与单位转换 (合并自外弹道方程 / 火箭仿真程序).

本模块作为 ``ballistic_sim`` 全包唯一的物理常数真值来源，集中管理 WGS84 椭球、
ISA 标准大气、地球自转、J2 引力摄动等常数。

合并说明
--------
- 原 ``ballistics.constants`` 中的 ``G0_STANDARD`` 与原 ``lvsim.constants`` 中的
  ``G0`` 物理含义相同，统一采用 ``G0_STANDARD`` 作为标准重力加速度。
- 原 ``lvsim.constants`` 中的 ``RE_EQ`` 与原 ``ballistics.constants`` 中的
  ``WGS84_A`` 取值相同，统一采用 ``WGS84_A`` 作为 WGS84 长半轴。
- 原 ``ballistics.constants`` 中的 ``R_DRY`` 与原 ``lvsim.constants`` 中的
  ``R_AIR`` 取值相同，统一采用 ``R_AIR`` 作为干空气比气体常数。
- 保留 ``EARTH_RADIUS_MEAN`` 作为地球平均半径参考值，与 WGS84 长/短半轴不重复。
"""

import numpy as np

# ==============================================================================
# 一、地球几何与引力场 (WGS84 / EGM)
# ==============================================================================

WGS84_A = 6378137.0
"""WGS84 椭球长半轴 / 赤道半径 (m)。"""

WGS84_F = 1.0 / 298.257223563
"""WGS84 扁率 f (无量纲)。"""

WGS84_E2 = WGS84_F * (2.0 - WGS84_F)
"""WGS84 第一偏心率平方 e^2 = 2f - f^2 (无量纲)。"""

WGS84_B = 6356752.314245
"""WGS84 椭球短半轴 / 极半径 (m)，= WGS84_A * sqrt(1 - WGS84_E2)。"""

RE_EARTH_MEAN = 6371000.0
"""地球平均半径 (m)，供粗略估算使用。"""

GM_EARTH = 3.986004418e14
"""地心引力常数 μ = G·M (m^3/s^2)，WGS84/EGM 标准值。"""

J2_EARTH = 1.08262668e-3
"""地球二阶带谐系数 J2 (无量纲)，描述扁率引起的主要引力摄动。"""

OMEGA_EARTH = 7.292115e-5
"""地球自转角速度 ω (rad/s)，对应恒星日；用于 ECI/ECEF 变换与科氏项。"""

# ==============================================================================
# 二、标准重力与 ISA 大气
# ==============================================================================

G0_STANDARD = 9.80665
"""标准重力加速度 g0 (m/s^2)，定义值；用于 Isp/有效排气速度换算。"""

R_AIR = 287.05287
"""干空气比气体常数 R = R_universal / M_air (J/(kg·K))。"""

GAMMA_AIR = 1.4
"""干空气定压/定容比热比 γ = cp/cv (无量纲)，用于声速 a = sqrt(γ·R·T)。"""

T0_ISA = 288.15
"""ISA 海平面标准温度 T0 (K)，即 15 °C。"""

P0_ISA = 101325.0
"""ISA 海平面标准气压 P0 (Pa)。"""

RHO0_ISA = P0_ISA / (R_AIR * T0_ISA)
"""ISA 海平面标准大气密度 ρ0 (kg/m^3)，由理想气体状态方程推导。"""

# ==============================================================================
# 三、角度换算
# ==============================================================================

DEG2RAD = np.pi / 180.0
"""角度 -> 弧度换算因子 (rad/deg)。"""

RAD2DEG = 180.0 / np.pi
"""弧度 -> 角度换算因子 (deg/rad)。"""

# ==============================================================================
# 四、湿度 / 大气成分 (可选高级大气模型)
# ==============================================================================

MOLAR_MASS_DRY = 0.0289644
"""干空气摩尔质量 (kg/mol)。"""

MOLAR_MASS_VAPOR = 0.01801528
"""水汽摩尔质量 (kg/mol)。"""

EPSILON_VAPOR = MOLAR_MASS_VAPOR / MOLAR_MASS_DRY
"""水汽与干空气摩尔质量比 (≈ 0.622，无量纲)。"""
